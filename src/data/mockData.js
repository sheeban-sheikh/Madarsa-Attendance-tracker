// Helper to generate seed attendance records for the past 7 days
export const generateSeedAttendance = () => {
  const records = [];
  const today = new Date();
  
  for (let i = 0; i < 7; i++) {
    const date = new Date(today);
    date.setDate(today.getDate() - i);
    const dateString = date.toISOString().split("T")[0];
    
    // Seed attendance for m1, m2, m3, m4, m5
    const activeMadarsas = ["m1", "m2", "m3", "m4", "m5"];
    
    activeMadarsas.forEach((madarsaId) => {
      const studentsInMadarsa = mockStudents.filter(s => s.madarsaId === madarsaId);
      const studentRecords = {};
      
      studentsInMadarsa.forEach((student, index) => {
        // High present rate ~80%
        // Using a deterministic formula based on indices and days to avoid random shifts on reloads
        const isPresent = ((index + i) % 6) !== 0; 
        studentRecords[student.id] = isPresent;
      });
      
      records.push({
        date: dateString,
        madarsaId,
        records: studentRecords
      });
    });
  }
  
  return records;
};
